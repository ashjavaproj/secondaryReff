package Task1;

public class C_University {

	private String uname;
	private int uid;
	private long mobno;
	private B_College clg;
	public String getUname() {
		return uname;
	}
	public void setUname(String uname) {
		this.uname = uname;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public long getMobno() {
		return mobno;
	}
	public void setMobno(long mobno) {
		this.mobno = mobno;
	}
	public B_College getClg() {
		return clg;
	}
	public void setClg(B_College clg) {
		this.clg = clg;
	}
	
}
